# O objetivo deste programa é exibir duas mensagens na tela:
# - A primeira mensagem é diretamente inserida na função print().
# - A segunda mensagem é armazenada em uma variável e depois exibida.

print('Olá Mundo! 1')  # Exibe a mensagem 'Olá Mundo! 1' diretamente na tela. O comando print() é utilizado para mostrar informações ao usuário.

msg = 'Olá Mundo! 2'  # Armazena a string 'Olá Mundo! 2' na variável 'msg'. Variáveis são usadas para armazenar dados que podem ser reutilizados no programa.

print(msg)  # Exibe o conteúdo da variável 'msg', que é 'Olá Mundo! 2'. O comando print() é utilizado novamente para mostrar a mensagem armazenada na variável.

# Nota:
# - No primeiro print(), a mensagem é fornecida diretamente como um argumento da função print().
# - No segundo print(), a mensagem é armazenada em uma variável antes de ser exibida. Isso é útil quando você precisa reutilizar a mensagem ou manipular o texto antes da exibição.
# - A variável 'msg' pode ser usada em qualquer lugar do código após sua definição, desde que esteja dentro do escopo onde foi criada.
